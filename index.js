// ---switch case. multi cases
let age = 10;
switch (age) {
  case 5:
    console.log("less than 10", age);
    break;
  case 6:
  case 7:
  case 10:
    console.log("equal to 10");
    break;
  default:
    console.log("invalid value");
}


